import { RNG } from "./rng.mjs";

const COLORS = ["Black", "Orange", "White", "Purple", "Gold"];
const colorsHex = {
  Black: "#1A1A1A",
  Orange: "#FC4F05",
  White: "#E6E6E6",
  Purple: "#C37AFC",
  Gold: "#FFBA33",
};

const offsetSeed = 1200000;

function scoring(value, sizes) {
  let i = 0;
  for (; i < sizes.length - 1; i += 2) {
    if (value < sizes[i + 1]) return sizes[i];
  }
  return sizes[i];
}

function getMotion(axis, amp, acc) {
  return [
    scoring(amp, ["minor", 0.15, "", 0.35, "major"]),
    acc > 5 ? "dancing" : acc === 1 ? "" : acc == 2 ? "quick" : "fast",
    ["horizontal", "vertical", "zoom"][axis],
  ]
    .filter(Boolean)
    .join(" ");
}

export function generate(index) {
  const random = RNG(index + offsetSeed);
  const word = WORDS[index];
  const bgNoiseSeed = 9 * random();
  const bgNoise = random() * random();
  const bgNoiseFreq = 2 * random() + 0.1 / (0.005 + random());
  const bgNoiseMotion =
    random() * mix(bgNoise, 2 * random() * random(), random());
  const bgRadial = random() / (0.001 + random());
  let color = Math.floor(COLORS.length * random() * random());
  const motion = [0, 0, 0];
  const motionPhase = ["cos", "cos", "cos"];
  const motionAcc = [1, 1, 1];
  const lightPos = 8 * (random() - 0.5) * random();
  let Motions = [];
  if (random() < 0.9) {
    const axis1 = Math.floor(3 * random() * random());
    const axis2 = Math.floor(3 * random());
    let amp1 = 0.2 + 0.8 * random() * random();
    let amp2 = Math.max(0, 0.5 * random() - 0.25);
    const acc1 = random() < 0.03 ? 6 : 1 + Math.floor(3 * random() * random());
    const acc2 = random() < 0.03 ? 6 : 1 + Math.floor(3 * random() * random());
    amp1 = mix(amp1, amp1 / acc1, random());
    amp2 = mix(amp2, amp2 / acc2, random());
    if (amp2 > 0.05) {
      motion[axis2] = amp2;
    }
    motionAcc[axis1] = acc1;
    motion[axis1] = amp1;
    Motions.push(getMotion(axis1, amp1, acc1));
    if (axis2 !== axis1 && motion[axis2]) {
      Motions.push(getMotion(axis2, amp2, acc2));
      motionPhase[axis2] = "sin";
      motionAcc[axis2] = acc2;
    }
  }
  const motionComplex =
    Motions.length > 0 && random() < 0.2
      ? 1 + Math.floor(2 * random() * random())
      : 0;
  const bgColor =
    color < 2 && random() < 0.3
      ? random() < 0.7
        ? "Orange"
        : "Purple"
      : random() < 0.1
      ? "Black"
      : "White";

  const bgOrangeNoise =
    bgNoiseFreq > 1 && color < 2 && bgColor !== "Orange" && random() < 0.2;
  if (bgColor === COLORS[color] && random() < 0.9) {
    color = (color + 1) % COLORS.length;
  }
  const negativeScreen = random() < 0.05;
  const halfnegativeScreen = random() < 0.005;
  const swivelRotate = random() < 0.7;
  const slowSwivelRotate = random() < 0.2;
  const scrollingScreen = swivelRotate ? random() < 0.05 : random() < 0.3;
  const blinkingScreen =
    !scrollingScreen && (swivelRotate ? random() < 0.03 : random() < 0.2);
  let swivelAngle = 5 * (random() - 0.5);
  if (Math.abs(swivelAngle) < 0.6) {
    swivelAngle = 0;
  }
  const swivel = swivelRotate
    ? slowSwivelRotate
      ? "slowly animated"
      : "animated"
    : swivelAngle === 0
    ? "fully opened"
    : Math.abs(swivelAngle) > Math.PI / 2
    ? "almost closed"
    : "almost opened";
  let sentence;
  let sticker;
  let hasEmojis;
  let hasEmojiSticker;

  if (swivelRotate || swivelAngle) {
    const pick = SENTENCES_ARRAY[Math.floor(random() * 2048)];
    if (pick) {
      hasEmojis = hasEmojis || pick.emoji;
      sentence = pick.text;
    }
  }

  let screenAnimation =
    random() < 0.02 * (swivelRotate ? 0.2 : 1)
      ? [random(), random(), random(), random(), random()]
      : 0;

  const btnAnimate = random() < 0.02 + (screenAnimation ? 0.2 : 0);

  let swivelPlotted =
    random() < 0.1 ? [random(), random(), random(), random(), random()] : 0;

  if ((!sentence || random() < 0.01) && (swivelRotate || swivelAngle)) {
    const pick = emojis_array[Math.floor(emojis_array.length * random())];
    if (pick) {
      hasEmojis = hasEmojis || pick.emoji;
      hasEmojiSticker = pick.emoji;
      sticker = pick.text;
    }
  }

  const stickerPosY =
    0.16 * (random() < 0.5 ? -1 : 1) * (1 - random() * random());
  const stickerPosX = !sentence
    ? 1.4 - 0.7 * random() * random()
    : 1.35 + 0.1 * random();

  const opts = {
    word,
    sentence,
    sticker,
    colorName: COLORS[color],
    motion,
    motionPhase,
    motionAcc,
    motionComplex,
    blinkingScreen,
    negativeScreen,
    halfnegativeScreen,
    scrollingScreen,
    slowSwivelRotate,
    swivelRotate,
    swivelAngle,
    bgColor,
    lightPos,
    stickerPosY,
    stickerPosX,
    swivelPlotted,
    screenAnimation,
    btnAnimate,
    bgNoise,
    bgNoiseFreq,
    bgNoiseMotion,
    bgNoiseSeed,
    bgRadial,
    bgOrangeNoise,
    hasEmojis,
    hasEmojiSticker,
  };
  const features = {
    Word: word,
    "Swivel Text": sentence,
    Sticker: sticker,
    Color: COLORS[color],
    Background: bgColor,
    "Background Noise": bgOrangeNoise ? "Orange Noise" : "",
    "Swivel Plotted": swivelPlotted ? "Yes" : "No",
    "Camera Motion Complex": motionComplex ? "Yes" : "No",
    "Camera Motion": Motions.length === 0 ? "None" : Motions.join(" with "),
    "Primary Motion": Motions[0],
    "Secondary Motion": Motions[0],
    "Screen Color": halfnegativeScreen
      ? "half-negative"
      : negativeScreen
      ? "negative"
      : "normal",
    "Screen Animation": screenAnimation
      ? "complex"
      : blinkingScreen
      ? "blinking"
      : scrollingScreen
      ? "scrolling"
      : "none",
    Swivel: swivel,
    "Buttons Animated": btnAnimate ? "Yes" : "No",
    Emojis: hasEmojis ? "Yes" : "No",
  };
  const attributes = [];
  for (let trait_type in features) {
    const value = features[trait_type];
    if (value) {
      attributes.push({ trait_type, value });
    }
  }

  const background_color = colorsHex[bgColor];
  const ipfs =
    "https://gateway.pinata.cloud/ipfs/QmUeTAQMPSQgVSBkjbWNAe3WCakNC2kvNCPAhvor18Em49/";
  const metadata = {
    name: `GNSP '${word}'`,
    description: `The collection has 2048 NFTs, one for each unique word in the BIP39 wordlist. '${word}' is the ${
      index + 1
    }${index === 0 ? "st" : index === 1 ? "nd" : "th"} word of this wordlist.

GNSP is short for Generative Nano S+ â€“ Ledger's new hardware wallet â€“ which this collection is celebrating. There are rarity aspects in the colors, background, animations and swivel engraved content.

Using a hardware wallet is important to secure your crypto and secure your NFTs. Made by @greweb â€“ December 2021 â€“ Kudos to the Ledger team`,
    image: ipfs + index + ".jpg",
    animation_url: ipfs + index + ".mp4",
    external_url: "https://greweb.me/gnsp/" + index,
    attributes,
    background_color,
  };

  return {
    opts,
    metadata,
  };
}

function mix(a, b, x) {
  return a * (1 - x) + b * x;
}

